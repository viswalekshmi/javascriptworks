function displayCakes(cakes){

    let htmlData=``

    for (let c of cakes){

        htmlData+=`
                    <div class="relative aspect-[16/9]  w-auto rounded-md md:aspect-auto md:h-[400px]">
          <img
            src="${c.image}"
            alt="AirMax Pro"
            class="z-0 h-full w-full rounded-md object-cover"
          />
          <div class="absolute inset-0 rounded-md bg-gradient-to-t from-gray-900 to-transparent"></div>
          <div class="absolute bottom-4 left-4 text-left">
            <h1 class="text-lg font-semibold text-white">${c.title}</h1>
            <p class="mt-2 text-sm text-gray-300">
              ${c.difficulty}
            </p>
            <button class="mt-2 inline-flex cursor-pointer items-center text-sm font-semibold text-white">
              Shop Now →
            </button>
          </div>
        </div>
        
        `
    }

    document.querySelector("#root").innerHTML=htmlData
}


function filterCakesByDifficulty(event){

    let choice=event.target.value;

    let items=cakes.filter(c=>c.difficulty==choice)

    displayCakes(items)

}

populateSelectBox()
function populateSelectBox(){

    let choices=new Set(cakes.map(c=>c.difficulty))

    let selectBox=document.querySelector("#selectBox")

    for (let ch of choices){

        let optiontag=document.createElement("option")

        optiontag.value=ch

        optiontag.text=ch

        selectBox.appendChild(optiontag)
    }


}


